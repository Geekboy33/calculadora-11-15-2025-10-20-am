import pino from "pino";
import { CFG } from "./config.js";

export const log = pino({
  level: CFG.LOG_LEVEL,
  transport: {
    target: "pino-pretty",
    options: {
      colorize: true,
      translateTime: "SYS:standard",
      ignore: "pid,hostname"
    }
  }
});

// Utility loggers for specific components
export const logAI = log.child({ component: "AI-Bandit" });
export const logWorker = log.child({ component: "Worker" });
export const logOracle = log.child({ component: "Oracle" });
export const logDEX = log.child({ component: "DEX" });
export const logExecutor = log.child({ component: "Executor" });




